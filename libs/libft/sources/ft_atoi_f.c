#include "libft.h"

int		ft_atoi_f(char *str)
{
	int		i;
	int		s;
	int		r;

	r = 0;
	i = 0;
	s = 1;
	while (str[i] >= 0 && str[i] <= 32)
		i++;
	if (str[i] == '-')
		s = -1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		r = r * 10 + str[i] - '0';
		i++;
	}
	free(str);
	return (s * r);
}
